package pro.sky.java.course1.lesson12;
public class Book {
    private String author;
    private int amountSheets;

    public Book(String author, int amountSheets) {
        this.author = author;
        this.amountSheets = amountSheets;
    }
    public String getAuthor() {
        return author;
    }
    public int getAmountSheets() {
        return amountSheets;
    }

    public void setAutor() {
        this.author = author;
    }
    public void setAmountSheets() {
        this.amountSheets = amountSheets;
    }


}
